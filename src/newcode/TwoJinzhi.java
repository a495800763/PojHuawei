package newcode;

import java.util.Scanner;

/**
 * 华为机试 转换成二进制之后的1 的个数
 */
public class TwoJinzhi {
    public static void main(String[] args) {
        Scanner in = new Scanner(System.in);
        while (in.hasNext()) {
            int i = in.nextInt();
            int count = 0;
            if (i == 1 || i == 2) {
                System.out.print(1);
            } else {
                while (i >= 2) {
                    int i1 = i % 2;
                    if (i1 == 1) {
                        count += 1;
                    }
                    i = i / 2;
                }
                System.out.print(count + 1);
            }
            System.out.print('\n');
        }
    }
}
